uint16_t utils_crc16(uint8_t *buf, uint32_t length)
{
    int i;
    unsigned short shift = 0xFFFF, data = 0, val = 0;
    for (i = 0; i < length; i++) {
        if ((i % 8) == 0) {
            data = (*buf++) << 8;
        }
        val = shift ^ data;
        shift = shift << 1;
        data = data << 1;
        if (val & 0x8000) {
            shift = shift ^ 0x1021;
        }
    }
    return shift;
}


uint32_t utils_crc32(uint8_t *buf, uint32_t length)
{
    uint8_t i;
    uint32_t crc = 0xFFFFFFFF;

    while (length--) {
        crc ^= (uint32_t) (*buf++) << 24;
        for (i = 0; i < 8; ++i) {
            if (crc & 0x80000000) {
                crc = (crc << 1) ^ 0x04C11DB7;
            } else {
                crc <<= 1;
            }
        }
    }
    return crc;
}
